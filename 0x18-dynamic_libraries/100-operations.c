#include "operations.h"

/**
 * add - add two numbers
 * @a: the 1st number
 * @b: the 2nd number
 *
 * Return: the sum of a and b
 */
int add(int a, int b)
{
	return (a + b);
}


/**
 * sub - subtract two numbers
 * @a: the 1st number
 * @b: the 2nd number
 *
 * Return: the difference of a and b
 */
int sub(int a, int b)
{
	return (a - b);
}


/**
 * mul - multiply two numbers
 * @a: the 1st number
 * @b: the 2nd number
 *
 * Return: the product of a and b
 */
int mul(int a, int b)
{
	return (a * b);
}


/**
 * div - divide two numbers
 * @a: the 1st number
 * @b: the 2nd number
 *
 * Return: the quotient of a and b
 */
int div(int a, int b)
{
	return (a / b);
}


/**
 * mod - mod two numbers
 * @a: the 1st number
 * @b: the 2nd number
 *
 * Return: the modulo of a and b
 */
int mod(int a, int b)
{
	return (a % b);
}
